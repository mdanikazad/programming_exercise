#include <stdio.h>

int main()
{
    
  int a, b;
  scanf("%d %d",&a, &b);
  int sum, sub, div, mul;
  sum = a + b;
  sub = a - b;
  div = a / b;
  mul = a * b;
  
  printf("summation: %d\n", sum);
  printf("substraction: %d\n", sub);
  printf("division: %d\n", div);
  printf("multiplication: %d\n", mul);
  
  
  return 0;
} 

 
                                     
